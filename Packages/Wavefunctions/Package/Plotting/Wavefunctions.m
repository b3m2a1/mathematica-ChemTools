(*WFPlotManipulate*)



WFPlotManipulate[
  {
    engs_,
    wfns_,
    pot_
    },
 ops:OptionsPattern[]
 ]:=
  Manipulate[
    Replace[
      Show[
        {
          If[engs[[i]]=!=None,
            engs[[i]],
            Nothing
            ],
          If[pot=!=None,
            pot,
            Nothing
            ],
          wfns[[i]]
          },
        Options[wfns[[i]]]
        ],
      {
        Except[_Graphics|_Graphics3D|_Legended]:>
          Failure["...", <||>]
        }
      ],
    {{i, 1, "\[Psi]"}, 1, Length@wfns, 1}
    ]


