(* ::Subsubsection::Closed:: *)
(*WFPlotOptionValue*)



WFPlotOptionValue[opName_, ops_, f_:None, default_:Automatic]:=
  Lookup[Flatten@{ops}, opName,
    Lookup[Options[f], opName, 
      Lookup[$WFPlotOptions, opName, default]
      ]
    ]


