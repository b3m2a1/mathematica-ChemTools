(*iWFPlot*)



iWFPlot[{energies_, gfs_}, pot_, ops:OptionsPattern[]]:=
  Module[
    {
      showE=WFPlotOptionValue["ShowEnergy",    {ops}, WFPlot, False],
      showP=WFPlotOptionValue["ShowPotential", {ops}, WFPlot, False],
      potS=WFPlotOptionValue["PotentialStyle", {ops}, WFPlot, Automatic],
      eS=WFPlotOptionValue["EnergyStyle", {ops}, WFPlot, Automatic],
      pm=WFPlotOptionValue["PlotDisplayMode", {ops}, WFPlot, Automatic],
      grid=gfs[[1]]["Grid"],
      efuncs,
      pfunc,
      eplots,
      potPlot,
      basePlots,
      allPlots
      },
    pm=
      Switch[pm,
        Show|"Show",
          WFPlotShow,
        List|"List",
          WFPlotList,
        Manipulate|"Manipulate",
          WFPlotManipulate,
        _,
          PackageRaiseException[
            Automatic,
            "\"PlotDisplayMode\" `` not understood",
            pm
            ]
        ];
    basePlots=GFPlot[gfs, "PlotDisplayMode"->"List", ops];
    If[TrueQ@showE,
      efuncs=
        GridFunctionObject[grid, ConstantArray[#, Dimensions[grid]]&/@energies];
      eplots=GFPlot[eplots, PlotStyle->eS, "PlotDisplayMode"->"List"],
      eplots=ConstantArray[None, Length@basePlots];
      ];
    If[TrueQ@showP&&pot=!=None,
      pfunc=GridFunctionObject[grid, Flatten@pot];
      potPlot=WFPlot[pfunc, PlotStyle->potS, "PlotDisplayMode"->"Show"],
      potPlot=None;
      ];
    pm[
      {
        eplots,
        basePlots,
        potPlot
        }
      ]
    ]


