AtomGraph[obj:ChemObjPattern|ChemObjVectorPattern,
	edge:UndirectedEdge|DirectedEdge:UndirectedEdge]:=
	With[{atoms=
		Select[
			ChemGet[
				DeleteDuplicates@Flatten@
					ChemGet[obj,"Bonds"],
				"Atoms"
				],
			AllTrue[#,
				MatchQ[Alternatives@@Flatten@{obj}]]&
			]
		},
		Block[{atomGraphOrdering=<||>},
			With[{a=
				If[KeyMemberQ[atomGraphOrdering,First@#],
					Reverse@#,
					#]},
				atomGraphOrdering[First@a]=Null;
				edge@@a
				]&/@atoms//Graph
			]
		];


atomGraphEvents[basevs_,bs_,as_,DepthFirstScan]:=
	With[{evs=
		Merge[
			Replace[Flatten@{basevs},{
				(k:("BackEdge"|"FrontierEdge"|
					"ForwardEdge"|"CrossEdge")->f_):>
					k->(f[#/.bs]&),
				({k:"BackEdge"|"FrontierEdge"|
					"ForwardEdge"|"CrossEdge",
					c:ChemObjPattern}->f_):>
					(If[MatchQ[#/.bs,c],f[c]]&),
				({k_,c:ChemObjPattern}->f_):>
					k->(If[MatchQ[#,c],f[c]]&),
				(c:ChemObjPattern->f_):>
					If[MemberQ[c,as],
						Thread[
							{"DiscoverVertex",
								"UnvisitedVertex","VisitedVertex",
								"PrevisitVertex","PostvisitVertex"}->
								(If[MatchQ[#,c],f[c]]&)
							],
						Thread[
							{"BackEdge","FrontierEdge",
								"ForwardEdge","CrossEdge"}->
								 (If[MatchQ[#/.bs,c],f[c]]&)
							]
						]
				},1],
			Replace[f:{__}:>
				(Through[f[#]]&)
				]
			]},
		evs
		];


atomGraphEvents[basevs_,bs_,as_,BreadthFirstScan]:=
	Merge[
		Replace[Flatten@{basevs},{
			(k:("FrontierEdge"|"CycleEdge")->f_):>
				k->(f[#/.bs]&),
			({k:("FrontierEdge"|"CycleEdge"),
				c:ChemObjPattern}->f_):>
				(If[MatchQ[#/.bs,c],f[c]]&),
			({k_,c:ChemObjPattern}->f_):>
				k->(If[MatchQ[#,c],f[c]]&),
			(c:ChemObjPattern->f_):>
				If[MemberQ[as,c],
					Thread[
						{"DiscoverVertex",
							"UnvisitedVertex","VisitedVertex",
							"PrevisitVertex","PostvisitVertex"}->
							(If[MatchQ[#,c],f[c]]&)
						],
					Thread[
						{"FrontierEdge","CycleEdge"}->
							(If[MatchQ[#/.bs,c],f[c]]&)
						]
					]
			},
			1],
		Replace[f:{__}:>
			(Through[f[#]]&)
			]
		];


AtomGraphEvents[obj:ChemObjAllPattern,
	mode:BreadthFirstScan|DepthFirstScan:BreadthFirstScan,
	events_:{}
	]:=
	With[{g=AtomGraph[obj]},
		With[{
			as=VertexList@g,
			bs=EdgeList@g/.e:(a_\[UndirectedEdge]b_):>(
				(e|Reverse@e)->First@AtomGetBonds[a,b]
				),
			basevs=
				Flatten@events
			},
			atomGraphEvents[basevs,bs,as,mode]
			]
		];


AtomGraphScan[
	obj:ChemObjAllPattern,
	startingNode:ChemObjPattern|None:None,
	mode:BreadthFirstScan|DepthFirstScan|Automatic:Automatic,
	events_:{}]:=
	With[{scanF=
		Replace[mode,
			Automatic:>
				If[
					FreeQ[events,
						("BackEdge"|"FrontierEdge"|
							"ForwardEdge"|"CrossEdge")],
					BreadthFirstScan,
					DepthFirstScan
					]
				]},
		If[startingNode===None,
			scanF[AtomGraph@obj,
				AtomGraphEvents[obj,scanF,events]
				],
			scanF[AtomGraph@obj,startingNode,
				AtomGraphEvents[obj,scanF,events]
				]
			]
		]


AtomGraphicShapeFunction2D[
	color_,
	position_,
	radius_,
	label_,
	style_
	]:=
	{
		{
			Replace[style, None->Nothing],
			color,
			Disk[position[[;;2]], radius]
			},
		Replace[label,
			{
				None->
					Nothing,
				Text[s_]:>
					Text[
							Style[
								s,
								If[ColorDistance[color, Black]>.7, Black, White]
								],
							position[[;;2]]
							],
				e_:>
					Inset[
						Style[e,
							If[ColorDistance[color, Black]>.7, Black, White]],
						position[[;;2]]
						]
				}]
		}


Options[AtomGraphic]=
	{
		"AtomicRadius"->Automatic,
		"AtomColor"->Automatic,
		"AtomStyle"->Automatic,
		"RadiusScaling"->.15,
		"AtomLabel"->
			Automatic,
		"AtomShapeFunction"->
			Automatic
		};
AtomGraphic[obj:ChemObjAllPattern,ops:OptionsPattern[]]:=
	With[{
		color=
			Replace[OptionValue["AtomColor"],
				Automatic:>AtomColor@obj
				],
		style=
			Replace[OptionValue["AtomStyle"],
				Automatic:>None
				],
		position=
			Flatten[List@ChemGet[obj,"Position"], 1],
		lab=
			Replace[OptionValue["AtomLabel"],
				{
					Automatic:>ChemGet[obj, "AtomLabel", None],
					"Numbered":>Range[Length@Flatten@obj]
					}
				],
		fn=
			Replace[OptionValue["AtomShapeFunction"],
				Automatic->AtomGraphicShapeFunction2D
				],
		radius=
			Flatten[List@Replace[
				OptionValue@"AtomicRadius",
				Automatic:>(
					OptionValue@"RadiusScaling"*
						QuantityMagnitude@
							UnitConvert[ChemGet[obj,"Radius"],"Angstroms"]
					)
				],
			1
			]
		},
		MapThread[
			fn,
			{
				Take[
					Flatten[ConstantArray[color, Length[position]], 1],
					Length[position]
					],
				position,
				Take[
					Flatten[ConstantArray[radius, Length[position]], 1],
					Length[position]
					],
				Take[
					Flatten[ConstantArray[lab, Length[position]], 1],
					Length[position]
					],
				Take[
					Flatten[ConstantArray[style, Length[position]], 1],
					Length[position]
					]
				}
			]
		];


AtomGraphicShapeFunction3D[
	color_,
	position_,
	radius_,
	label_,
	style_
	]:=
	{
		{
			Replace[style,
				None->Nothing
				],
			color,
			Sphere[position, radius]
			},
		Replace[label,
			{
				None->
					Nothing,
				Text[s_]:>
					Text[
							Style[
								s,
								If[ColorDistance[color, Black]>.7, Black, White]
								],
							position
							],
				e_:>
					Map[
						Inset[
							Style[
								e,
								If[ColorDistance[color, Black]>.7, Black, White]
								],
							position+#
							]&,
						(.15+radius)*
							If[radius>.5,
								Join[IdentityMatrix[3], -IdentityMatrix[3]],
								{{0,0,1}}
								]
						]
				}]
		}


